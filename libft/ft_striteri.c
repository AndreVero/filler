#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int i;

	i = 0;
	if (s == NULL || f == NULL)
		return ;
	while (s != NULL && *s != '\0')
	{
		f(i, s);
		i++;
		s++;
	}
}
