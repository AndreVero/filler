#include "libft.h"

char		*ft_strcat(char *s1, const char *s2)
{
	char	*p;
	int		i;

	p = NULL;
	i = ft_strlen(s1);
	p = s1;
	while (*s2)
	{
		s1[i] = *s2;
		i++;
		s2++;
	}
	s1[i] = '\0';
	return (p);
}
